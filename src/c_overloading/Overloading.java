package c_overloading;

//constructor overloading via sample code

public class Overloading {
	
	   Overloading()
	   {

		System.out.println();
		
		}

		Overloading(int b)
		{

		System.out.println("enter the digit "+ b);
		
		}

		Overloading(String c)
		{

		System.out.println("enter the string " + c);
		
		}

}


